/**
 * Translate a single text string based on language.
 * Returns original text for 'zh' or if no translation exists.
 */
export function translateData(text: string | undefined | null, lang: Language): string {
  if (!text) return ''
  if (lang === 'zh') return text
  return dataMap[text] || text
}

/**
 * Deep-translate an object's string fields.
 * Translates specified fields or all string fields if no keys specified.
 */
export function translateObject<T extends Record<string, unknown>>(
  obj: T,
  lang: Language,
  fields?: string[]
): T {
  if (lang === 'zh') return obj
  const result = { ...obj }
  const keysToTranslate = fields || Object.keys(obj)
  for (const key of keysToTranslate) {
    const value = result[key]
    if (typeof value === 'string') {
      (result as Record<string, unknown>)[key] = dataMap[value] || value
    }
  }
  return result
}

/**
 * Deep-translate an array of objects.
 */
export function translateArray<T extends Record<string, unknown>>(
  arr: T[],
  lang: Language,
  fields?: string[]
): T[] {
  if (lang === 'zh') return arr
  return arr.map((item) => translateObject(item, lang, fields))
}

export default dataMap
